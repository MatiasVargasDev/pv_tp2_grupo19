/*
 * Calcular y mostrar por consola el área de un círculo cuyo radio = 7,5
*/

let radio = 7.5
const PI = Math.PI
const AREA = PI * Math.pow(radio, 2)

console.log(`El área del círculo es: ${AREA}`)
console.log(`El área del círculo es: ${AREA.toFixed(2)}`) // Para mostrar el area con 2 decimales
